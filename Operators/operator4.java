
public class operator4 {

    public static void main(String[] args){
        int x = 10;
        int y = 7;

        System.out.println(++x);
        System.out.println(++y);

        System.out.println(--x);
        System.out.println(--y);

        System.out.println(x);
        System.out.println(y);
    }
    
}
